fn coord(f: &Faker, lo: i64, hi: i64) -> String {
    let micro = f.rng.random_int(lo * 1_000_000, hi * 1_000_000, 1);
    let deg = micro / 1_000_000;
    let frac = (micro % 1_000_000).abs();
    format!("{}.{:06}", deg, frac)
}

fn latitude(f: &Faker, _l: &str) -> String {
    coord(f, -90, 90)
}

fn longitude(f: &Faker, _l: &str) -> String {
    coord(f, -180, 180)
}

fn latlng(f: &Faker, l: &str) -> String {
    format!("{},{}", latitude(f, l), longitude(f, l))
}

/// Pick a random land coordinate filtered by country code derived from locale.
/// Returns "lat,long".
fn local_latlng(f: &Faker, locale: &str) -> String {
    // Derive country code from locale (e.g. "en_US" -> "US", "fr_FR" -> "FR")
    let country_code = locale
        .split('_')
        .nth(1)
        .map(|s| s.to_uppercase())
        .unwrap_or_default();

    // Collect matching indices to avoid allocation of references
    let matches: Vec<usize> = LAND_COORDS
        .iter()
        .enumerate()
        .filter(|(_, c)| c.3.eq_ignore_ascii_case(&country_code))
        .map(|(i, _)| i)
        .collect();

    let entry = if !matches.is_empty() {
        let pick = f.rng.below(matches.len());
        &LAND_COORDS[matches[pick]]
    } else {
        &LAND_COORDS[f.rng.below(LAND_COORDS.len())]
    };
    format!("{},{}", entry.0, entry.1)
}

/// Pick a random land coordinate and return "lat,long,place,country,timezone".
fn location_on_land(f: &Faker) -> String {
    let idx = f.rng.below(LAND_COORDS.len());
    let e = &LAND_COORDS[idx];
    format!("{},{},{},{},{}", e.0, e.1, e.2, e.3, e.4)
}

pub fn dispatch(f: &Faker, locale: &str, name: &str) -> Option<String> {
    Some(match name {
        "latitude" => latitude(f, locale),
        "longitude" => longitude(f, locale),
        "coordinate" => coord(f, -180, 180),
        "latlng" => latlng(f, locale),
        "local_latlng" => local_latlng(f, locale),
        "location_on_land" => location_on_land(f),
        _ => return None,
    })
}
